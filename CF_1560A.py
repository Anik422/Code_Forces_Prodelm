n = int(input())
for i in range(n):
    x = int(input())
    count = 0
    m = 1
    result = 1
    while count != x:
        if m % 3 != 0:
            if m % 10 != 3:
                result = m
                count += 1
                m += 1
            else:
                m += 1
        else:
            m += 1
    print(result)


